package selenium_day1;

public class ControlStatements {

	public static void main(String args[]) {
		int a = 0;
	
//		For Condition
		
		for(int i = 10; i>=a; i--) {
			if(i==7) {
				System.out.println(i);
				continue;
			}
			System.out.println(i);

				
				
	
	}
		
	
}
}